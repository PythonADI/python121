# 1 only end (start from 0, go up to 10, increase step by 1)
for i in range(10):
    print(i)

print("+++++++++")
# 2 start and end (start from 5, go up to 10, increase step by 1)
for i in range(5, 10):
    print(i)



print("+++++++++")
# 3 start, end and step (start from 0, go up to 10, increase step by 2)
for i in range(0, 10, 2):
    print(i)


print("+++++++++")
for i in range(10, 0, -2):
    print(i)
